//Break and Continue



//Break in js:-
for (let index = 0; index <= 20; index++) {
    if(index == 5){
        console.log("Arisha Chaudhary ");
      break
    }
    console.log(index);
    
}


//Continue in js:-
for (let index = 0; index <= 20; index++) {
    if(index == 5){
        console.log("Arisha Chaudhary ");
      continue
    }
    console.log(index);
    
}